
                                    1. wire ( провідник / дріт )
// пов'язує шини між собою
// ! - не може зберірігати данні
 
asssign // зв'язує шини
-------------------
wire a // провідник a
asssign a = b; // пов'язує a та b, робить ці виходи однаковими
assign a = ~b; // пов'язує a та b, робить ці виходи комплементарними
[=]
wire a = b;
-------------------
wire [3:0]c; // оголошення 4 дротів: c[3], c[2], c[1], c[0] - шини с
wire [3:0] e;   // якщо e також 4-бітна шина
asssign e = c; // з'єднання шин e та c

assign c[1] = e[3]


