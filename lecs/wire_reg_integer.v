                                    2. reg ( регістр ) ( flip-flop )
// пов'язує шини між собою
// ! - може зберірігати данні
-------------------
reg [3:0] m; // 4 bit регістр [m] | m {[m3] [m2] [m1] [m0]}
reg [0:31] int_a; // 32 bit регістр [int_a] | int_a {[int_a0] [int_a1] ... [int_a31]}
// ! - через нестандартний запис [0:31] / [31:0]

int_a = c; // asssign не треба


